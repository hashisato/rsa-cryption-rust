//! # rsa-cryption-rust
//!
//! This crate provides simple RSA encryption/decryption and signing/verification functionalities
//! using the [`rsa`](https://docs.rs/rsa/) crate.
//!
//! ## Features
//! - RSA encryption and decryption
//! - RSA signing and verification
//! - Console-based demonstration functions
//!
//! ## Usage
//! Import the modules and use the provided functions for encryption, decryption, signing, and verification.
//!
//! See each module's documentation for details.

pub mod rsa_cryption;
pub mod rsa_signature;
pub mod rsa_server_client;
